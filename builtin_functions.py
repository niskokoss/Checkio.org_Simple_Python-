all(iterable) # Если все условия будут True вернет True, если хотябы 1-но будет False вернет False
              # Подойдет для каких либо проверок 
def all(iterable):
    for element in iterable:  # Проверяет наличие элемента в итерируемом объекте
        if not element:  # Если это не элемент
            return False
    return True
print(all("zxcv"))

