any(iterable) # Если хоть 1-но условие будет True - вернет True, если все False - вернет False
              # Подойдет для каких либо проверок 
def any(iterable):
    for element in iterable: # Проверяет наличие элемента в итерируемом объекте
        if element: # Если это элемент
            return True
    return False
  
